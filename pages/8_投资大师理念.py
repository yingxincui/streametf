# 罗伯特·清崎 (Robert Kiyosaki)
st.header("💰 罗伯特·清崎 (Robert Kiyosaki)")

with st.expander("📚 核心理念", expanded=True):
    st.markdown("""
    ### 🏠 现金流象限
    - **E象限（雇员）**：为钱工作，收入有限
    - **S象限（自雇）**：为自己工作，时间换钱
    - **B象限（企业主）**：拥有系统，让别人为你工作
    - **I象限（投资者）**：让钱为你工作
    
    ### 💡 投资理念
    - **资产 vs 负债**：资产把钱放进你的口袋，负债把钱从你的口袋拿走
    - **被动收入**：建立能够产生被动收入的资产
    - **财务教育**：投资于自己的财务教育
    - **风险管理**：学会管理风险，而不是避免风险
    
    ### 🎪 经典语录
    > "富人买入资产，穷人买入负债，中产阶级买入他们认为是资产的负债。"
    
    > "你的房子不是资产，它是负债。"
    
    > "投资于你的头脑，这是你能做的最好的投资。"
    """)

# 沃伦·巴菲特 (Warren Buffett)
st.header("🎯 沃伦·巴菲特 (Warren Buffett)")

with st.expander("📚 核心理念", expanded=True):
    st.markdown("""
    ### 🎯 价值投资
    - **安全边际**：以低于内在价值的价格买入
    - **长期思维**：如果你不愿意持有一只股票10年，那就不要持有10分钟
    - **护城河**：投资有持久竞争优势的企业
    - **管理层质量**：投资于优秀的管理团队
    
    ### 💰 投资原则
    - **简单易懂**：只投资你能理解的企业
    - **合理价格**：好公司也要在合理价格买入
    - **集中投资**：把鸡蛋放在少数几个篮子里，然后好好看管
    - **耐心等待**：市场先生有时会提供机会
    
    ### 🎪 经典语录
    > "价格是你付出的，价值是你得到的。"
    
    > "别人贪婪时我恐惧，别人恐惧时我贪婪。"
    
    > "时间是优秀企业的朋友，平庸企业的敌人。"
    """)

# 彼得·林奇 (Peter Lynch)
st.header("🔍 彼得·林奇 (Peter Lynch)")

with st.expander("📚 核心理念", expanded=True):
    st.markdown("""
    ### 🎯 成长投资
    - **自下而上**：从个股开始分析，而不是宏观经济
    - **实地调研**：投资你了解的公司
    - **成长性**：寻找能够持续增长的企业
    - **估值合理**：即使是好公司，也要在合理价格买入
    
    ### 💡 投资策略
    - **10倍股**：寻找能够涨10倍的股票
    - **行业轮动**：在不同行业间轮动投资
    - **分散投资**：适度分散，降低风险
    - **长期持有**：给好公司时间成长
    
    ### 🎪 经典语录
    > "投资你了解的公司。"
    
    > "股票市场是投票机，不是称重机。"
    
    > "最好的投资机会往往就在你身边。"
    """)

